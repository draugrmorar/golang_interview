func fourSum(nums []int, target int) [][]int {
	res := make([][]int, 0, len(nums))
	var pos int
	var r []int
	slices.Sort(nums)
	for i := 0; i < len(nums)-3; i++ {
		for j := i + 1; j < len(nums)-2; j++ {
			for k := j + 1; k < len(nums)-1; k++ {
				pos = target - nums[i] - nums[j] - nums[k]
				for l := k + 1; l < len(nums); l++ {
					if nums[l] == pos {
						r = []int{nums[i], nums[j], nums[k], nums[l]}
						if !IsExist(res, r) {
							res = append(res, r)
						}
						break
					}
				}
			}
		}
	}
	return res
}

func IsExist(r [][]int, el []int) bool {
	for _, v := range r {
		for i := range v {
			if v[i] != el[i] {
				break
			} else if i == len(v)-1 {
				return true
			}
		}
	}
	return false
}

